let x = 3.21
let y = 81
let z

z = Math.round(x) // arredonda pra cima ou pra baixo
console.log(`arredonda pra cima ou pra baixo${z}`)


z = Math.floor(x) // arredonda pra baixo
console.log(`arredonda pra baixo ${z}`)


z = Math.ceil(x) // arredonda pra cima
console.log(`arredonda pra cima ${z}`)


z = Math.trunc(x) // apaga apos a virgula
console.log(`apaga apos a virgula ${z}`)


z = Math.sqrt(y) // raiz quadrada
console.log(`raiz quadrada ${z}`)


z = Math.abs(y) // numero absoluto (transforma em positivo)
console.log(`transforma em positivo ${z}`)